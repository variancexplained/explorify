import inspect
import logging
from datetime import datetime

import pytest

from explorify.eda.bivariate.mixed import BivariateEffectSizeAnalyzer

# ------------------------------------------------------------------------------------------------ #
# pylint: disable=missing-class-docstring, line-too-long
# ------------------------------------------------------------------------------------------------ #
# ------------------------------------------------------------------------------------------------ #
logger = logging.getLogger(__name__)
# ------------------------------------------------------------------------------------------------ #
double_line = f"\n{100 * '='}"
single_line = f"\n{100 * '-'}"


@pytest.mark.bivariate
@pytest.mark.mixed
class TestBivariateCategoricalNumericalAnalyzer:  # pragma: no cover
    # ============================================================================================ #
    def test_effect_size(self, credit, caplog):
        start = datetime.now()
        logger.info(
            f"\n\nStarted {self.__class__.__name__} {inspect.stack()[0][3]} at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        analysis = BivariateEffectSizeAnalyzer(data=credit)
        results = analysis.analyze(a_name="Education", b_name="Income")
        assert isinstance(results, float)
        logger.info(results)
        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            f"\n\nCompleted {self.__class__.__name__} {inspect.stack()[0][3]} in {duration} seconds at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(single_line)
