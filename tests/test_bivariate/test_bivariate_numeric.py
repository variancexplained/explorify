import inspect
import logging
import os
from datetime import datetime

import pytest

from explorify.eda.bivariate.numeric import NumericBivariateAnalysis
from explorify.eda.stats.inferential.base import StatTestResult

# ------------------------------------------------------------------------------------------------ #
# pylint: disable=missing-class-docstring, line-too-long
# ------------------------------------------------------------------------------------------------ #
# ------------------------------------------------------------------------------------------------ #
logger = logging.getLogger(__name__)
# ------------------------------------------------------------------------------------------------ #
double_line = f"\n{100 * '='}"
single_line = f"\n{100 * '-'}"


@pytest.mark.bivariate
@pytest.mark.numeric
class TestNumericBivariateAnalysis:  # pragma: no cover
    # ============================================================================================ #
    def test_validate_input(self, dataset, caplog):
        start = datetime.now()
        logger.info(
            f"\n\nStarted {self.__class__.__name__} {inspect.stack()[0][3]} at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        analysis = NumericBivariateAnalysis(data=dataset)
        with pytest.raises(ValueError):
            analysis.validate_input(var1="app_name", var2="rating")
        with pytest.raises(ValueError):
            analysis.validate_input(var1="rating", var2="category")
        with pytest.raises(ValueError):
            analysis.validate_input(var1="bogus", var2="rating")
        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            f"\n\nCompleted {self.__class__.__name__} {inspect.stack()[0][3]} in {duration} seconds at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(single_line)

    # ============================================================================================ #
    def test_correlation(self, dataset, caplog):
        start = datetime.now()
        logger.info(
            f"\n\nStarted {self.__class__.__name__} {inspect.stack()[0][3]} at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        analysis = NumericBivariateAnalysis(data=dataset)
        result = analysis.correlation_coefficient(var1="rating", var2="review_length")
        assert isinstance(result, StatTestResult)
        logger.info(result)

        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            f"\n\nCompleted {self.__class__.__name__} {inspect.stack()[0][3]} in {duration} seconds at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(single_line)

    # ============================================================================================ #
    def test_regression(self, dataset, caplog):
        start = datetime.now()
        logger.info(
            f"\n\nStarted {self.__class__.__name__} {inspect.stack()[0][3]} at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        analysis = NumericBivariateAnalysis(data=dataset)
        result = analysis.regression_analysis(var1="rating", var2="review_length")
        assert isinstance(result, dict)
        logger.info(result)

        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            f"\n\nCompleted {self.__class__.__name__} {inspect.stack()[0][3]} in {duration} seconds at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(single_line)

    # ============================================================================================ #
    def test_image(self, plt, dataset, caplog):
        caplog.set_level(logging.INFO)
        start = datetime.now()
        logger.info(
            f"\n\nStarted {self.__class__.__name__} {inspect.stack()[0][3]} at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        analysis = NumericBivariateAnalysis(data=dataset)
        os.makedirs("tests/plots/bivariate", exist_ok=True)
        plt.saveas = "tests/plots/bivariate/numeric_scatterplot.png"
        _ = analysis.visualize(var1="review_length", var2="rating")
        plt.show()

        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            f"\n\nCompleted {self.__class__.__name__} {inspect.stack()[0][3]} in {duration} seconds at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(single_line)
