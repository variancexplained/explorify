import inspect
from datetime import datetime
import pytest
import logging
import pandas as pd

from explorify.eda.stats.inferential.association import KendallsTauAnalysis


# ------------------------------------------------------------------------------------------------ #
logger = logging.getLogger(__name__)
# ------------------------------------------------------------------------------------------------ #
double_line = f"\n{100 * '='}"
single_line = f"\n{100 * '-'}"


@pytest.mark.stats
@pytest.mark.ind
@pytest.mark.kendalls
class TestKendallsTau:  # pragma: no cover
    # ============================================================================================ #
    def test_kendalls_tau(self, credit, caplog):
        start = datetime.now()
        logger.info(
            "\n\nStarted {} {} at {} on {}".format(
                self.__class__.__name__,
                inspect.stack()[0][3],
                start.strftime("%I:%M:%S %p"),
                start.strftime("%m/%d/%Y"),
            )
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        test = KendallsTauAnalysis(data=credit, a="Education", b="Credit Rating")
        test.run()
        result = test.result
        assert isinstance(result.value, float)
        assert isinstance(result.pvalue, float)
        assert isinstance(result.data, pd.DataFrame)
        assert isinstance(result.strength, str)
        logging.debug(result)
        logging.debug(result.report())

        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            "\nCompleted {} {} in {} seconds at {} on {}".format(
                self.__class__.__name__,
                inspect.stack()[0][3],
                duration,
                end.strftime("%I:%M:%S %p"),
                end.strftime("%m/%d/%Y"),
            )
        )
        logger.info(single_line)
